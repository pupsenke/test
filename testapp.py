# Программа для проверки, является ли треугольник равносторонним

def is_equilateral(a, b, c):
    # Проверяем, что все стороны равны
    return a == b == c

def main():
    print("Проверка на равносторонний треугольник")
    a = float(input("Введите длину стороны a: "))
    b = float(input("Введите длину стороны b: "))
    c = float(input("Введите длину стороны c: "))

    # Проверим, что стороны могут образовать треугольник
    if a <= 0 or b <= 0 or c <= 0:
        print("Длины сторон должны быть положительными числами.")
        return

    if a + b <= c or a + c <= b or b + c <= a:
        print("С такими сторонами треугольник не существует.")
        return

    if is_equilateral(a, b, c):
        print("Треугольник равносторонний.")
    else:
        print("Треугольник не равносторонний.")

if __name__ == "__main__":
    main()
